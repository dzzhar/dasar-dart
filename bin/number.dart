void main() {
  // deklarasi tipe data integer (bilangan bulat)
  int number1 = 20;
  int number2 = 10;
  print(number1);
  print(number2);
  print(number1 + number2);

  // deklarasi tipe data double (bilangan desimal)
  double number3 = 10.5;
  double number4 = 20.5;
  print(number3);
  print(number4);
  print(number3 + number4);

  /**
   * deklarasi tipe data num
   * - merubah value bilangan bulat menjadi bilangan real
   * - merubah value bilangan real menjadi bilangan bulat
   */
  num number5 = 40;
  number5 = 40.5;
  num number6 = 50.5;
  number6 = 50;
  print(number5);
  print(number6);

  // tipe data boolean
  bool benar = true;
  bool salah = false;
  print(benar);
  print(salah);
}
